#!/usr/bin/env python3
"""Regenerate the canonical final score summary from tracked inputs."""

from __future__ import annotations

import argparse
import json
from decimal import Decimal
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parents[1]
DEFAULT_MANIFEST = ROOT / "experiments" / "final" / "manual_review_scores.json"
DEFAULT_OUTPUT = ROOT / "experiments" / "final" / "final_grades.md"
WEIGHT_PHASE1 = Decimal("0.75")
WEIGHT_CHALLENGE = Decimal("0.25")
ONE_DECIMAL = Decimal("0.1")
FORMULA_TOLERANCE = Decimal("0.05")


def load_json(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text())


def dec(value: Any) -> Decimal:
    return Decimal(str(value))


def format_score(value: Decimal) -> str:
    return f"{value.quantize(ONE_DECIMAL):.1f}"


def rounded_decimal(value: Decimal) -> Decimal:
    return Decimal(format_score(value))


def build_markdown(manifest_path: Path) -> str:
    manifest = load_json(manifest_path)
    overall_phase1: list[Decimal] = []
    overall_published: list[Decimal] = []
    sections: list[str] = []

    lines = [
        f"# Final Scores - Grok (`{manifest['model']}`)",
        "",
        "This file is the canonical public score summary for the April 8, 2026 final run.",
        "",
        (
            "Generated by `python scripts/build_final_scores.py` from "
            "`experiments/final/manual_review_scores.json` plus the tracked raw April 8 run JSONs."
        ),
        "",
        "## Scoring Rule",
        "",
        "Each prompt has one published usefulness score.",
        "",
        "- `Published prompt score = 0.75 * Phase 1 score + 0.25 * challenge review score`",
        "- If a prompt did not go to challenge, the published score remains the Phase 1 score",
        "- The Phase 1 baseline is still tracked in the report because it shows what Grok volunteered on its own",
        "",
        "Canonical supporting artifacts:",
        "- `report/main.pdf`",
        "- `report/main.tex`",
        "- `experiments/final/manual_review_scores.json`",
        "- `experiments/final/README.md`",
        "- `experiments/final/conversation_artifacts.md`",
        "- `experiments/final/improvement_loop.md`",
        "- `experiments/final/rescoring_notes.md`",
        "",
        (
            "Tracked raw April 8, 2026 run JSONs now live in this directory. "
            "See `experiments/final/README.md` for filenames, hashes, challenge coverage, and "
            "score-provenance notes."
        ),
        "",
        "## Generation Note",
        "",
        (
            "The prompt-level `Published Phase 1`, `Challenge Review`, and `Published` values below "
            "are complete for all 30 prompts and are generated from a machine-readable manual-review "
            "manifest plus the tracked raw April 8 run JSONs."
        ),
        "",
        "- Values already surfaced in the report stayed unchanged.",
        "- Unchallenged prompts keep `Published Phase 1 = Published`.",
        "- Retrospectively completed prompt-level review values are documented in `rescoring_notes.md`.",
        "- The builder validates challenge coverage against the raw run JSONs before writing this file.",
        "",
        "---",
    ]

    for domain in manifest["domains"]:
        raw_path = manifest_path.parent / domain["raw_run_file"]
        raw = load_json(raw_path)
        results = raw["results"]
        if raw["metadata"]["area"] != domain["slug"]:
            raise ValueError(f"Area slug mismatch for {domain['name']}: {raw['metadata']['area']}")
        if raw["metadata"]["prompt_count"] != len(domain["prompts"]):
            raise ValueError(f"Prompt count mismatch for {domain['name']}")
        entries_by_index = {entry["index"]: entry for entry in results}

        prompt_rows = [
            "",
            f"## {domain['name']}",
            "",
            (
                f"**Phase 1 baseline: {domain['expected_phase1_baseline']:.1f}/100** | "
                f"**Published: {domain['expected_published_score']:.1f}/100** | "
                f"**Delta: {domain['expected_published_score'] - domain['expected_phase1_baseline']:+.1f}**"
            ),
            "",
            "| # | Prompt | Published Phase 1 | Published | Challenged | Headline read |",
            "|---|--------|-------------------|-----------|------------|---------------|",
        ]
        challenge_rows = [
            "",
            "| # | Prompt | Challenge Review | Formula check |",
            "|---|--------|------------------|---------------|",
        ]

        phase1_scores: list[Decimal] = []
        published_scores: list[Decimal] = []

        for prompt in domain["prompts"]:
            entry = entries_by_index.get(prompt["index"])
            if entry is None:
                raise ValueError(f"Missing raw entry {domain['name']} prompt {prompt['index']}")
            challenged = bool(entry["challenge_sent"])
            challenge_review = prompt["challenge_review"]
            if challenged != (challenge_review is not None):
                raise ValueError(
                    f"Challenge mismatch for {domain['name']} prompt {prompt['index']}: "
                    f"raw={challenged} manifest={challenge_review is not None}"
                )

            phase1 = dec(prompt["published_phase1"])
            published = dec(prompt["published"])
            phase1_scores.append(phase1)
            published_scores.append(published)

            if challenged:
                review = dec(challenge_review)
                weighted = phase1 * WEIGHT_PHASE1 + review * WEIGHT_CHALLENGE
                if abs(weighted - published) > FORMULA_TOLERANCE:
                    raise ValueError(
                        f"Published score mismatch for {domain['name']} prompt {prompt['index']}: "
                        f"weighted={weighted} published={published}"
                    )
                challenge_value = f"{review:.1f}"
                formula = f"`0.75*{phase1:.1f} + 0.25*{review:.1f} = {published:.1f}`"
                challenged_label = "Yes"
            else:
                if phase1 != published:
                    raise ValueError(
                        f"Unchallenged prompt mismatch for {domain['name']} prompt {prompt['index']}"
                    )
                challenge_value = "--"
                formula = "Unchallenged"
                challenged_label = "No"

            prompt_rows.append(
                f"| {prompt['index']} | {prompt['title']} | {phase1:.1f} | {published:.1f}/100 | "
                f"{challenged_label} | {prompt['headline']} |"
            )
            challenge_rows.append(
                f"| {prompt['index']} | {prompt['title']} | {challenge_value} | {formula} |"
            )

        phase1_avg = sum(phase1_scores) / Decimal(len(phase1_scores))
        published_avg = sum(published_scores) / Decimal(len(published_scores))
        expected_phase1 = dec(domain["expected_phase1_baseline"])
        expected_published = dec(domain["expected_published_score"])

        if phase1_avg.quantize(ONE_DECIMAL) != expected_phase1:
            raise ValueError(
                f"Phase 1 baseline mismatch for {domain['name']}: "
                f"{format_score(phase1_avg)} != {expected_phase1:.1f}"
            )
        if published_avg.quantize(ONE_DECIMAL) != expected_published:
            raise ValueError(
                f"Published score mismatch for {domain['name']}: "
                f"{format_score(published_avg)} != {expected_published:.1f}"
            )

        overall_phase1.append(phase1_avg)
        overall_published.append(published_avg)
        sections.append("\n".join(prompt_rows + challenge_rows + ["", "---"]))

    overall_phase1_avg = sum(overall_phase1) / Decimal(len(overall_phase1))
    overall_published_avg = sum(overall_published) / Decimal(len(overall_published))

    lines.extend(sections)
    lines.extend(
        [
            "",
            "## Overall",
            "",
            "| Domain | Phase 1 baseline | Published score | Delta | Headline read |",
            "|--------|------------------|-----------------|-------|---------------|",
        ]
    )

    for domain, phase1_avg, published_avg in zip(manifest["domains"], overall_phase1, overall_published):
        delta = rounded_decimal(published_avg) - rounded_decimal(phase1_avg)
        lines.append(
            f"| {domain['name']} | {format_score(phase1_avg)}/100 | {format_score(published_avg)}/100 | "
            f"{delta:+.1f} | {domain['summary_headline']} |"
        )

    overall_delta = rounded_decimal(overall_published_avg) - rounded_decimal(overall_phase1_avg)
    lines.append(
        f"| **Overall** | **{format_score(overall_phase1_avg)}/100** | "
        f"**{format_score(overall_published_avg)}/100** | "
        f"**{overall_delta:+.1f}** | "
        f"**{manifest['overall_headline']}** |"
    )
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--manifest", type=Path, default=DEFAULT_MANIFEST)
    parser.add_argument("--output", type=Path, default=DEFAULT_OUTPUT)
    parser.add_argument(
        "--check",
        action="store_true",
        help="Check that the generated markdown matches the checked-in output.",
    )
    args = parser.parse_args()

    content = build_markdown(args.manifest)
    output = args.output
    if args.check:
        if not output.exists():
            raise SystemExit(f"Output file does not exist: {output}")
        current = output.read_text()
        if current != content:
            raise SystemExit(f"{output} is out of date. Re-run python scripts/build_final_scores.py")
        print(f"{output} is up to date.")
        return 0

    output.write_text(content)
    print(f"Wrote {output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
